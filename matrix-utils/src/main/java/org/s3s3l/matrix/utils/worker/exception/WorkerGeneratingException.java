package org.s3s3l.matrix.utils.worker.exception;

public class WorkerGeneratingException extends RuntimeException {

    public WorkerGeneratingException() {
    }

    public WorkerGeneratingException(String arg0) {
        super(arg0);
    }

    public WorkerGeneratingException(Throwable arg0) {
        super(arg0);
    }

    public WorkerGeneratingException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public WorkerGeneratingException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
        super(arg0, arg1, arg2, arg3);
    }
    
}
